		<h3 class="h3">Beschreibung</h3>
		<p>
Das Abrechnungsportal vereint veschiedene Abrechnungen.
Dies sind: Tourenabrechnung, Arbeitsabrechnung, Aufgabenabrechnung,
Geldabrechnung, Firmenabrechnung.
Diese können für einfache Abrechnungen verwendet werden.
Es können Firmen, Admins und mehrere Benutzer registriert werden.
Eine Anmeldung ist für 25 € / Monat möglich. Zahlbar über PayPal.
Kündbar monatlich.</p>
		<p>
- Tourenabrechnung: Es können Touren abgerechnet werden. Als GPX und Bild-Datei
mit Darstellung der Strecke. Weiterhin als Übersichtsseite für jeden Benutzer. Ausgabe als
PDF-Dokument mit Auswertung.<br>
- Arbeitsabrechnung: Arbeiten können mit Nachweis (Bild, PDF) und GPX-Datei abgerechnet werden.
Weiterhin gibt es eine Übersichtsseite für Firma und Benutzer. Ausgabe als PDF-Dokument
mit Auswertung.<br>
- Aufgabenabrechnung: Aufgaben können mit Nachweis (Bild, PDF) abgerechnet werden. Weiterhin gibt es eine Übersichtsseite für Firma und Benutzer. Ausgabe als PDF-
Dokument mit Auswertung.<br>
- Geldabrechnung: Gelder können mit Rechnung und Zahlung abgerechnet werden.
Ausgabe als PDF-Dokument mit Auswertung. Es ist eine EA-Abrechnung für das Finanzamt
möglich. Darstellung der Gelder als Graph.<br>
- Firmenabrechnung: Eine Mitarbeiterabrechnung ist möglich. Als PDF-Dokument in der Auswertung.
		</p>
